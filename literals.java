public class literals {
    public static void main(String[] args) {
        int age = 24;
        byte agee = 34;
        short ageee = 44;
        char che = 'd';
        float nume = 21.89f;

        System.out.println(age);
        System.out.println(agee);
        System.out.println(ageee);
        System.out.println(che);
        System.out.println(nume);
    }
    
}
